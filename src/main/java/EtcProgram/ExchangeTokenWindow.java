    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ImageIcon Cat_Warning = new ImageIcon("C://My Learing//Java//DIT102//GachaProgram//photo//Cat Warning.gif");
        DB_Con db = new DB_Con();
        try {
            int money = Integer.parseInt(Money.getText());
            int Tk = Integer.parseInt(Token.getText());
            String sql = String.format("Update user set user_exchange = ((select user_exchange from user where user_id='%s') + '%d') where user_id ='%s'", SignUpWindow.get_id(), Tk, SignUpWindow.get_id());
            String sql1 = String.format("Update user set user_money = ((select user_money from user where user_id='%s') - '%d') where user_id ='%s' ", SignUpWindow.get_id(), (Tk * 20), SignUpWindow.get_id());
            if (Tk >= 1) {
                if (Method.get_money(SignUpWindow.get_id()) >= Tk*20) {
                    if (db.execute(sql) && db.execute(sql1)) {
                        ImageIcon Cat = new ImageIcon("C://My Learing//Java//DIT102//GachaProgram//photo//Cat Success.gif");
                        JOptionPane.showMessageDialog(null, "Success!", "", JOptionPane.INFORMATION_MESSAGE, Cat);
                        STB.setText(Method.get_exchange(SignUpWindow.get_id()) + "");
                        Money.setText(Method.get_money(SignUpWindow.get_id()) + "");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "You Don't have enoght Money!", "", JOptionPane.INFORMATION_MESSAGE, Cat_Warning);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please input Numeric (Start at 1) without Negative mark!", "", JOptionPane.INFORMATION_MESSAGE, Cat_Warning);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Something was Wrong!", "", JOptionPane.INFORMATION_MESSAGE, Cat_Warning);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

