    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // TODO add your handling code here:
        ImageIcon Cat_Warning = new ImageIcon("C://My Learing//Java//DIT102//GachaProgram//photo//Cat Warning.gif");
        DB_Con db = new DB_Con();
        try {
            String Id = IDUser.getText();
            String OPass = OldPass.getText();
            String COPass = COldPass.getText();
            String sql = String.format("Select * from user Where user_id ='%s' and user_password ='%s'", Id, OPass);
            ResultSet rs = db.getResultSet(sql);
            if (OPass.equals(COPass)) {
                if (rs.next()) {
                    ImageIcon Cat = new ImageIcon("C://My Learing//Java//DIT102//GachaProgram//photo//Cat Success.gif");
                    JOptionPane.showMessageDialog(null, "Success!", "", JOptionPane.INFORMATION_MESSAGE, Cat);
                    NewPasswordForChangePasswordWindow NPW = new NewPasswordForChangePasswordWindow();
                    this.dispose();
                    NPW.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Password Not Match!", "", JOptionPane.INFORMATION_MESSAGE, Cat_Warning);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Iccorrect ID or Password!", "", JOptionPane.INFORMATION_MESSAGE, Cat_Warning);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Something was Wrong!", "", JOptionPane.INFORMATION_MESSAGE, Cat_Warning);
        }
    }//GEN-LAST:event_SubmitActionPerformed

